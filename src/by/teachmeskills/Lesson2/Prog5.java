package by.teachmeskills.Lesson2;

public class Prog5 {
    public static void main(String[] args) {
        int numcolor = 1;
        switch (numcolor) {
            case 1:
                System.out.println("Цвет радуги - Красный");
                break;
            case 2:
                System.out.println("Цвет радуги - Оранжевый");
                break;
            case 3:
                System.out.println("Цвет радуги - Желтый");
                break;
            case 4:
                System.out.println("Цвет радуги - Зеленый");
                break;
            case 5:
                System.out.println("Цвет радуги - Голубой");
                break;
            case 6:
                System.out.println("Цвет радуги - Синий");
                break;
            case 7:
                System.out.println("Цвет радуги - Фиолетовый");
                break;

            default:
                System.out.println("Число не от 1 до 7");
        }
    }


}
